    private void initSorts(){
       DefaultListModel lst = new DefaultListModel();
       for(int i=0;i<lieu.getOccupant().getArmes().getSorts().getNbArme();i++)
       {
            lst.addElement(lieu.getOccupant().getArmes().getSorts().getArme(i));
       }
       sortApprendre.setModel(lst); 
    }
    
    
    
