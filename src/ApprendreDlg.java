    private void ApprendreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ApprendreActionPerformed
     
        
        if(sortApprendre.getSelectedIndex() != -1)
           {
            for(Arme ar : lieu.getOccupant().getArmes().getLstArmes()) //parcoure les armes             
            {   
                if(ar.getNom().equals(lieu.getOccupant().getArmes().getArme(sortApprendre.getSelectedIndex()).getNom())) // si nom sort = nom du sort sélectionnné à l'index de la jlist                                    
                js = new Sort(ar.getNom(), ar.getForce());   
            }
            act = new Apprentissage(js, pc, lieu);
            act.execute();
            ok = true;
            Resultat.setText(act.getDescriptif());
            Apprendre.setEnabled(false);
            
           }
           else
           {
               info.setText("Choisissez un sort !");
           }
    }//GEN-LAST:event_ApprendreActionPerformed

